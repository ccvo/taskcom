package com.taskui.models.ui;

/**
 *
 * @author Chuong Vo
 */
public final class Argument {

    private String name;
    private String value;

    public Argument() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
